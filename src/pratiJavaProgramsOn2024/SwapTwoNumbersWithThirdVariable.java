package pratiJavaProgramsOn2024;

public class SwapTwoNumbersWithThirdVariable {
	public static void main(String[] args) {
		int number1=34;
		int number2=45;
		System.out.println("Before Swapping " + number1 + " ... " + number2);
		int temp=number2;
		number2=number1;
		number1=temp;
		System.out.println("After Swapping " + number1 + " ... " + number2);
		
		
	}

}
